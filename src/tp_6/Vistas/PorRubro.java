    private void Combo(){
        jcElejirRubro.addItem("Comestible");
        jcElejirRubro.addItem("Limpieza");
        jcElejirRubro.addItem("Perfumeria");
    }
    
    private void borrarFilas(){
        int filas = jtRubro.getRowCount() - 1;
        
        for(int f = filas; f >= 0; f-- ){
            m.removeRow(f);
        }
    }

