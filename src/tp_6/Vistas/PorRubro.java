    private void jcElejirRubroItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jcElejirRubroItemStateChanged
        borrarFilas();
        for(Productos producto:DeTodoSA.lista){
            if(producto.getRubro().equals(jcElejirRubro.getSelectedItem()))
                m.addRow(new Object[]{
                    producto.getCodigo(),
                    producto.getDescripcion(),
                    producto.getPrecio(),
                    producto.getStock()});
        }
    }//GEN-LAST:event_jcElejirRubroItemStateChanged

